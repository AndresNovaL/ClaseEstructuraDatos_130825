        double velocidad = 89.5;
        double masa = 72.3;

        // Estructura de Datos tipo Char
        char a = 'J';
        char b = 'U';
        char c = 'L';
        char d = 'I';
        char e = 'O';

        // Estructura de Datos tipo String
        String saludo = "¡Bievenido a la clase!";
